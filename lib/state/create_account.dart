  Future<Null> uploadPictureAndInsertData() async {
    String name = nameController.text;
    String address = addressController.text;
    String phone = phoneController.text;
    String user = userController.text;
    String password = passwordController.text;
    String path =
        '${MyConstant.domain}/pichiimall/getUserWhereUser.php?isAdd=true&user=$user';
    print(
        '### name = $name,  address = $address, phone = $phone, user = $user, password = $password');

    await Dio().get(path).then((value) async {
      print('## value ===>>> $value');
      if (value.toString() == 'null') {
        if (file == null) {
          // No Avatar
          processInsertMySQL(name: name, address: address, phone: phone, user: user, password: password);
        } else {
          // Have Avatar
          String apiSaveAvatar =
              '${MyConstant.domain}/pichiimall/saveAvatar.php';
          int i = Random().nextInt(100000);
          String nameAvatar = 'avatar$i.jpg';
          Map<String, dynamic> map = Map();
          map['file'] =
              await MultipartFile.fromFile(file!.path, filename: nameAvatar);
          FormData data = FormData.fromMap(map);
          await Dio().post(apiSaveAvatar, data: data).then((value) {
            avatar = '/pichiimall/avatar/$nameAvatar';
            processInsertMySQL(name: name, address: address, phone: phone, user: user, password: password);
          });
        }
      } else {
        MyDialog().normalDialog(context, 'User name False !!!',
            'This User name already used. Please change new User name');
      }
    });
  }

  Future<Null> processInsertMySQL(
      {String? name,
      String? address,
      String? phone,
      String? user,
      String? password}) async {
    print('### Process insert mySQL and avatar ==>> $avatar');
    String apiInsertUser =
        '${MyConstant.domain}/pichiimall/insertUser.php?isAdd=true&name=$name&type=$typeUser&address=$address&phone=$phone&user=$user&password=$password&avatar=$avatar&lat=$lat&lng=$lng';
    await Dio().get(apiInsertUser).then((value) {
      if (value.toString() == 'true') {
        Navigator.pop(context);
      } else {
        MyDialog().normalDialog(
            context, 'Create new User name False!!!', 'Please try again...');
      }
    });
  }

